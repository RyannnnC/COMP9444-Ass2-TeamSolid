def preprocessing(sample):
    """
    Called after tokenising but before numericalising.
    """
    def isword(str):
        if len(str)<=1:
            return False
        for s in str:
            if s in 'aeiou':
                return True
        return False

    result = []
    symbols = [",",".","?","!","(",")","$","~","n't","'ve","'m'"]
    for data in sample:
        for sym in symbols:
            data = data.replace(sym,"")
        if data.isalpha()and len(data)>1 and isword(data):
            result.append(data)

    return result

